package org.xuzhu.personal_manager.pojo;

public class Task {
    private int id;
    private String name;

    Task() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
